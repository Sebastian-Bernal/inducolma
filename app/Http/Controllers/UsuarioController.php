    {
        return $request->all();
        $usuario = new User();
        $usuario->id = $request->identificacionUsuario;
        $usuario->name = $request->name;
        $usuario->email = $request->email;
        $usuario->password = bcrypt('123456789');
        $usuario->save();
        return redirect()->route('usuarios.index');
    }

